// addresses for touchscreen
#define x_position_address 0b101
#define y_position_address 0b001
#define z1_address 0b011
#define z2_address 0b100
#define command_byte 0b10000001

void touchscreen_read(unsigned short *x, unsigned short *y, unsigned int *z){
    unsigned char x1, x2, y1, y2, z11, z12, z21, z22;
    
    XPT_CS = 0;
    spi_io(command_byte | (x_position_address <<4));
    x1 = spi_io(0x00);
    x2 = spi_io(0x00);
    XPT_CS = 1;
    
    XPT_CS = 0;
    spi_io(command_byte | (y_position_address <<4));
    y1 = spi_io(0x00);
    y2 = spi_io(0x00);
    XPT_CS = 1;
    
    XPT_CS = 0;
    spi_io(command_byte | (z1_address <<4));
    z11 = spi_io(0x00);
    z12 = spi_io(0x00);
    XPT_CS = 1;
    
    XPT_CS = 0;
    spi_io(command_byte | (z2_address <<4));
    z21 = spi_io(0x00);
    z22 = spi_io(0x00);
    XPT_CS = 1;
    
    *x = (x1<<5) | (x2>>3);
    *y = (y1<<5) | (y2>>3);
    *z = ((z11<<5)|(z12>>3)) - ((z21<<5)|(z22>>3)) + 4095;
}

void LCD_drawWord (unsigned short x, unsigned short y, unsigned short color, char * word){
    int i = 0;
    while(word[i]){
        LCD_drawChar(x + 5*i, y, color, word[i]);
        i++;
    }
}

void LCD_drawChar (unsigned short x, unsigned short y, unsigned short color, char c){
    // only do something if within bounds
    if(!(x > 235 || y > 312)){
        int char_x, char_y;
        
        for(char_x = 0; char_x < 5; char_x++){
            for(char_y = 0; char_y < 8; char_y++){
                if(((ASCII[c-0x20][char_x] >> char_y) & 0x01) == 1){
                    LCD_drawPixel(x + char_x, y + char_y, color);
                }
                // if not supposed to be colored, clear pixel just in case
                else{
                    LCD_drawPixel(x + char_x, y + char_y, ILI9341_GREEN);
                }
            }
        }
    }
}

void LCD_drawLine (unsigned short x1, unsigned short x2, unsigned short y1, unsigned short y2, unsigned short color){
    float x_length = x2-x1;
    float y_length = y2-y1;
    float longer_length = max(abs(x_length), abs(y_length));
    int i;
    for(i = 0; i<longer_length; i++){
        LCD_drawPixel(x1+(int)(x_length* i/longer_length), y1+(int)(y_length* i/longer_length), color);
    }
}

void LCD_drawRectangle (unsigned short x1, unsigned short x2, unsigned short y1, unsigned short y2, unsigned short color){
    int x_length = x2-x1;
    int y_length = y2-y1;
    int i, j, sign_x, sign_y;
    sign_x = (x2-x1 > 0) ? 1 : -1;
    sign_y = (y2-y1 > 0) ? 1 : -1;
    for(i=0; i<abs(x_length); i++){
        for(j=0; j<abs(y_length); j++){
            LCD_drawPixel(x1+i*sign_x, y1+j*sign_y, color);
        }
    }
}

